class Event(object):
    """this is a superclass for any events that might be generated by an
    object and sent to the EventManager"""

    def __init__(self):
        self.name = "Generic Event"


class TickEvent(Event):
    def __init__(self):
        Event.__init__(self)
        self.name = "CPU Tick Event"


class QuitEvent(Event):
    def __init__(self):
        Event.__init__(self)
        self.name = "Program Quit Event"


class GameStartEvent(Event):
    def __init__(self):
        Event.__init__(self)
        self.name = "Game Started Event"


class AppStartEvent(Event):
    def __init__(self):
        Event.__init__(self)
        self.name = "App Started Event"


class AppStartEvent(Event):
    def __init__(self):
        Event.__init__(self)
        self.name = "App Started Event"


class KeyboardEvent(Event):
    def __init__(self, key=None):
        Event.__init__(self)
        self.name = "Keyboard Event"
        self.key = key


class MouseEvent(Event):
    def __init__(self, button=None):
        Event.__init__(self)
        self.name = "Mouse Event"
        self.button = button


class SetCellEvent(Event):
    def __init__(self, posx=None, posy=None):
        Event.__init__(self)
        self.name = "Set Cell Event"
        self.posx = posx
        self.posy = posy


class CellAddedEvent(Event):
    def __init__(self, posx=None, posy=None):
        Event.__init__(self)
        self.name = "Cell Added Event"
        self.posx = posx
        self.posy = posy


